export function inferLanguage(value: string): JSONLanguageFormat | undefined {
  if (iso693Regex.test(value)) {
    return {
      name: "language",
      variant: "iso693-1",
    };
  }

  if (iso6932Regex.test(value)) {
    return {
      name: "language",
      variant: "iso693-2",
    };
  }

  if (iso693EnglishNames.includes(value)) {
    return {
      name: "language",
      variant: "english",
    };
  }

  if (iso693NativeNames.includes(value)) {
    return {
      name: "language",
      variant: "native",
    };
  }

  return undefined;
}
